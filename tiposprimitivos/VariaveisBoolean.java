package com.estudos.cursojava.tiposprimitivos;

public class VariaveisBoolean {

	public static void main(String[] args) {
		
		boolean verdade = true;
		
		boolean falsidade = false;
		
		System.out.println("O estado de 'valor na conta' é: " + falsidade);
		System.out.println("O estado de 'estudando' é: " + verdade);

	}

}
