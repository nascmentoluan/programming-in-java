package com.estudos.cursojava.variaveis;

public class Variaveis {
	
	public static void main(String[] args) {
			
		int idade = 20;
		String nome = "Luan";
		
		System.out.println("Meu nome é " + nome + " e eu tenho " + idade + " anos.");
		System.out.println("Não devo iniciar nomes de variáveis com símbolos (execeto _ e $), números e palavras reservadas");
		System.out.println("Covenção para nomes de variáveis:");
		System.out.println("1: <tipo de dado> nome");
		System.out.println("2: <tipo de dado> nomeVariavel");
		System.out.println("3: <tipo de dado> nomeVariavel = valor;");
		
	}
}
